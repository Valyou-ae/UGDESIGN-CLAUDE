export function getSeniorPersona(
  ethnicity: Ethnicity,
  sex: 'Female' | 'Male',
  size: Size
): Persona | undefined {
  const collection = sex === 'Female' ? SENIOR_FEMALE_PERSONAS : SENIOR_MALE_PERSONAS;
  return collection[ethnicity]?.[size]?.[0];
}

export function hasSeniorPersona(
  ethnicity: Ethnicity,
  sex: 'Female' | 'Male',
  size: Size
): boolean {
  return getSeniorPersona(ethnicity, sex, size) !== undefined;
}

export function getAllSeniorPersonas(): Persona[] {
  const all: Persona[] = [];
  
  const ethnicities: Ethnicity[] = ['White', 'Black', 'Hispanic', 'Asian', 'Indian', 'Southeast Asian', 'Indigenous', 'Diverse'];
  const sizes: Size[] = ['XS', 'S', 'M', 'L', 'XL', 'XXL', 'XXXL'];
  
  for (const ethnicity of ethnicities) {
    for (const size of sizes) {
      const female = getSeniorPersona(ethnicity, 'Female', size);
      const male = getSeniorPersona(ethnicity, 'Male', size);
      if (female) all.push(female);
      if (male) all.push(male);
    }
  }
  
  return all;
}

// Export combined personas
export const SENIOR_PERSONAS = {
  female: SENIOR_FEMALE_PERSONAS,
  male: SENIOR_MALE_PERSONAS
};
